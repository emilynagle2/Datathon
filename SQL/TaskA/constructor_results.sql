CREATE OR REPLACE TABLE EVENT.DATATHON_2025_TEAM_ETA.CONSTRUCTOR_RESULTS AS
SELECT 
    c.NAME AS CONSTRUCTOR_NAME,
    c.NATIONALITY AS CONSTRUCTOR_NATIONALITY,
    r.*   -- All columns from results
FROM 
    EVENT.DATATHON_2025_TEAM_ETA.RESULTS r
JOIN 
    EVENT.DATATHON_2025_TEAM_ETA.CONSTRUCTORS c ON r.CONSTRUCTORID = c.CONSTRUCTORID
ORDER BY 
    c.CONSTRUCTORID, r.RESULTID;
